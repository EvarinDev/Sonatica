/**
 * Enum representing different search platforms.
 * Each platform is associated with a specific search identifier.
 * 
 * @enum {string}
 */
export enum SearchPlatform {
	/** YouTube search platform */
	youtube = "ytsearch",
	/** YouTube Music search platform */
	"youtube music" = "ytmsearch",
	/** SoundCloud search platform */
	soundcloud = "scsearch",
	/** Deezer search platform */
	deezer = "dzsearch",
	/** Spotify search platform */
	spotify = "spsearch",
	/** Tidal search platform */
	tidal = "tdsearch",
	/** Apple Music search platform */
	"apple music" = "amsearch",
}
